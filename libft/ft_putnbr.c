#include "libft.h"

void	ft_putnbr(int n)
{
	int	nbr;

	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write (1, "-", 1);
		n = -n;
	}
	nbr = n;
	if (nbr > 9)
		ft_putnbr(n / 10);
	nbr = (n % 10) + 48;
	write (1, &nbr, 1);
}
